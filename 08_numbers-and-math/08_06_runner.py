# If a runner runs 10 miles in 30 minutes and 30 seconds,
# What is their average speed in kilometers per hour?
# (Tip: 1 mile = 1.6 km)

# Miles per minute = 10 / 30.5
# Miles per hour = Miles per minute * 60
# Km per hour = Miles per minute * 1.6

miles_per_minute = 10 / 30.5
miles_per_hour = miles_per_minute * 60
km_per_hour = miles_per_hour * 1.6
print(km_per_hour)